    private void addToMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToMenuActionPerformed
        // TODO add your handling code here:
        if ((!this.nuevoNom.getText().equals("")) && (!this.nuevoPrecio.getText().equals("")) && (!this.nuevoC.getText().equals("")) && (!this.c.getText().equals(""))) {
            try {
                int p = Integer.parseInt(this.nuevoPrecio.getText());
                int cc = Integer.parseInt(this.nuevoC.getText());
                int can = Integer.parseInt(this.c.getText());
                this.ingr = this.ingr.substring(0, this.ingr.length() - 1);
                String arreglo[] = this.ingr.split(";");

                int x = 0;
                int y = 0;
                int num;  //el numero que tendrá el plato
                //buscando el numero a brindarle al nuevo plato
                for (int i = 0; i < plato.size(); i++) {
                        x = this.plato.get(i).getNumero();  //obtiene numero del objeto
                }
                
                num=x+1;
                x++;
                for (int i = 0; i < bebida.size(); i++) {
                     this.bebida.get(i).setNumero(x);
                     x++;
                }

                this.nuevoPlato = new Plato(this.nuevoNom.getText(), num);
                this.nuevoPlato.setCalorias(cc);
                this.nuevoPlato.setIngredientes(arreglo);
                this.nuevoPlato.setPrecio(p);
                this.nuevoPlato.setVecesConsumido(this.nuevoPlato.getVecesConsumido() + 1);
                iiproyecto.main.restaurante.getMenu().agregarPlatillo(this.nuevoPlato); //agrega plato a menu
                Plato pl = this.nuevoPlato.clone();
                System.out.println(pl.getPrecio());
                orden.agregarPlato(pl, can); //un cllon del plato se va a la orden
                for (int i = 0; i < can; i++) {
                    pl = this.nuevoPlato.clone();
                    Pedido pedido = new Pedido(false); //espera a ser preparado
                    pedido.setPlato(pl); //se añaded el clon
                    enlace.añadirCola(pedido); //se agrega a la lista global de los pediods del restaurante
                }
                JOptionPane.showMessageDialog(this, " Se ha agregado el plato a la orden y al menu", " Notificación de sistema ", 1);
                //poner en no visibles los botones y labels otra vez
                this.n.setVisible(false);
                this.n2.setVisible(false);
                this.nuevoC.setVisible(false);
                this.nuevoC.setText("");
                this.c.setVisible(false);
                this.c.setText("");
                this.nuevoI.setVisible(false);
                this.nuevoI.setText("");
                this.nuevoNom.setVisible(false);
                this.nuevoNom.setText("");
                this.nuevoPrecio.setVisible(false);
                this.nuevoPrecio.setText("");
                this.n3.setVisible(false);
                this.n4.setVisible(false);
                this.n5.setVisible(false);  //acceso a botones y labels en falso
                this.n6.setVisible(false);
                this.fuera.setVisible(true);
                this.addIngredient.setVisible(false);
                this.addToMenu.setVisible(false);
            } catch (NullPointerException e) {
                JOptionPane.showMessageDialog(this, " Los datos ingresados son incorrectos  ", " Alerta de sistema ", 0);

            }
        } else {
            JOptionPane.showMessageDialog(this, " Los datos ingresados son incorrectos  ", " Alerta de sistema ", 0);
        }

    }//GEN-LAST:event_addToMenuActionPerformed

