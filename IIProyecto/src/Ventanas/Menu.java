    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:

        if ((!numeroPlato.getText().equals("")) && (!cant.getText().equals(""))) {

            try {
                int numero = Integer.parseInt(numeroPlato.getText());
                int cantidad = Integer.parseInt(cantid.getText());

                if (eleccion.getSelectedIndex() == 0) { //si se eligió un plato
                    Plato plato = enlace.getMenu().buscarPlatillo(numero);

                    if (plato != null) { //si el plato existe
                        plato.setVecesConsumido(plato.getVecesConsumido() + ((plato.getVecesConsumido()) * cantidad));
                        Plato pl = plato.clone();
                        System.out.println(pl.getPrecio());
                        orden.agregarPlato(pl, cantidad); //un cllon del plato se va a la orden
                        for (int i = 0; i < cantidad; i++) {
                            pl = plato.clone();
                            Pedido pedido = new Pedido(false); //espera a ser preparado
                            pedido.setPlato(pl); //se añaded el clon
                            enlace.añadirCola(pedido); //se agrega a la lista global de los pediods del restaurante
                        }
                        JOptionPane.showMessageDialog(this, " Producto agregado con éxito", "Notificación", 1);

                    } else {
                        JOptionPane.showMessageDialog(this, " El numero ingresado no pertenece a ningún plato del menu", "Alerta de sistema", 0);
                    }

                } else { //si fue una bebida
                    Bebida beverage = enlace.getMenu().buscarBebida(numero);

                    if (beverage != null) { //si la bebida existe
                        beverage.setVecesConsumido(beverage.getVecesConsumido() + ((beverage.getVecesConsumido()) * cantidad));
                        Bebida beb = beverage.clone();
                        System.out.println(beb.getPrecio());
                        orden.agregarBebida(beb, cantidad); //un clon de la bebida se va a la orden
                        for (int i = 0; i < cantidad; i++) { //se añade las veces que se pidio
                            Pedido pedido = new Pedido(false); //espera a ser preparado
                            pedido.setBebida(beb); //se añaded el clon
                            enlace.añadirCola(pedido); //se agrega a la lista global de los pediods del restaurante
                        }
                        JOptionPane.showMessageDialog(this, " Producto agregado con éxito", "Notificación", 1);

                    } else {
                        JOptionPane.showMessageDialog(this, " El numero ingresado no pertenece a ningúna bebida del menu", "Alerta de sistema", 0);
                    }

                }

            } catch (NumberFormatException e) {

                JOptionPane.showMessageDialog(this, " Alguno de los datos no representa un número", "Alerta de sistema", 0);
            } finally {

                numeroPlato.setText("");
                cantid.setText("");
            }

        } else {
            JOptionPane.showMessageDialog(this, " Los datos ingresados son incorrectos  ", " Alerta de sistema ", 0);
        }

    }//GEN-LAST:event_addActionPerformed

